package test_Coding;

public class Variable_Declaration_Initialization {
    public static void main(String[] args) {
        int i=25; //Declaration and initialization of Integer
        double d = 3.14; //Declaration and initialization of Double
        boolean b = true; //Declaration and initialization of boolean
        char c = 'A';  //Declaration and initialization of Character
        System.out.print("Integer: "+i); //Prints the integer value without new line
        System.out.print(" Double: "+d); //Prints the Double value without new line
        System.out.print(" Boolean: "+b); //Prints the Boolean value without new line
        System.out.print(" Character: "+c); //Prints the Character value without new line
    }
}
